//PROGRAM TO SHOW THE USE OF sizeof OPERATOR
#include<iostream>
using namespace std;
int main()
{
	int a;
	float b;
	double c;
	char d;
	
	cout<<"\nSIZE OF a = "<<sizeof(a);
	cout<<"\nSIZE OF b = "<<sizeof(b);
	cout<<"\nSIZE OF c = "<<sizeof(a);
	cout<<"\nSIZE OF d = "<<sizeof(d);
	cout<<"\nSIZE OF long double = "<<sizeof(long double);
	cout<<"\nSIZE OF float = "<<sizeof(float);
	
return 0;
}
